package board2;

public class CommentsBean {
	private int cono;
	private int bno;
	private String cowriter;
	private String cocontent;
	private String coreg_date;
	
	
	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public int getCono() {
		return cono;
	}
	public void setCono(int cono) {
		this.cono = cono;
	}
	public String getCowriter() {
		return cowriter;
	}
	public void setCowriter(String cowriter) {
		this.cowriter = cowriter;
	}
	public String getCocontent() {
		return cocontent;
	}
	public void setCocontent(String cocontent) {
		this.cocontent = cocontent;
	}
	public String getCoreg_date() {
		return coreg_date;
	}
	public void setCoreg_date(String coreg_date) {
		this.coreg_date = coreg_date;
	}
	
	

}
